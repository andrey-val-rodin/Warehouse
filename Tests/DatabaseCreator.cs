        private SQLiteConnection _connection;

        public void CreateDatabase(string filename)
        {
            // Create database from scratch
            if (File.Exists(filename))
                File.Delete(filename);

            _connection = new SQLiteConnection($"DataSource={filename};Mode=ReadWrite");
            _connection.Open();

            CreateTables();
            FillComponentType();
            FillComponent();
            FillProduct();
            FillProductComponent();
            _connection.Close();
        }

        private void CreateTables()
        {
            var query = @"
CREATE TABLE ComponentType (
    Id   INTEGER PRIMARY KEY AUTOINCREMENT,
    Name TEXT    NOT NULL
)
STRICT;

CREATE TABLE Component (
    Id              INTEGER PRIMARY KEY AUTOINCREMENT
                            NOT NULL,
    Name            TEXT    NOT NULL,
    Type            INTEGER NOT NULL
                            REFERENCES ComponentType (id),
    Amount          INTEGER NOT NULL
                            DEFAULT (0),
    AmountInUse     INTEGER NOT NULL
                            DEFAULT (0),
    Price           INTEGER,
    Ordered         INTEGER,
    ExpectedDate    TEXT,
    Details         TEXT
)
STRICT;

CREATE TABLE Product (
    Id   INTEGER PRIMARY KEY AUTOINCREMENT
                 NOT NULL,
    Name TEXT    NOT NULL
)
STRICT;

CREATE TABLE ProductComponent (
    ProductId   INTEGER REFERENCES Product (id) 
                        NOT NULL,
    ComponentId INTEGER	REFERENCES Component (id) 
                        NOT NULL,
    Amount      INTEGER
)
STRICT;

CREATE TABLE Fabrication (
    Id              INTEGER PRIMARY KEY AUTOINCREMENT
                            NOT NULL,
    ProductId       INTEGER REFERENCES Product (id),
    TableId         TEXT,
    Number          INTEGER,
    Status          INTEGER NOT NULL
                            DEFAULT (0),
    Client          TEXT,
    Details         TEXT,
    StartedDate     TEXT    NOT NULL
                            DEFAULT (DATE('now', 'localtime') ),
    ExpectedDate    TEXT,
    ClosedDate      TEXT
)
STRICT;
";
            using var command = new SQLiteCommand(query, _connection);
            command.ExecuteNonQuery();
        }

